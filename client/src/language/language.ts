export function getLanguage(lang: string): LanguageBase {
  if (!lang) {
    return new LangENUS();
  }
  switch (lang) {
    case Constants.LanguageKey.ENUS:
      return new LangENUS();

    // case Constants.Instance.LanguageKey.DE:
    // return new LangDE();

    default:
      return new LangENUS();
  }
}

export function getDefaultLanguage(): LanguageBase {
  return new LangENUS();
}

